async function seedProviders() {
  console.log('üå± Starting provider seeding...\n');

  try {
    // Clear existing providers (optional - comment out if you want to keep existing data)
    console.log('üóëÔ∏è  Clearing existing providers...');
    await prisma.provider.deleteMany({});
    console.log('‚úÖ Existing providers cleared\n');

    // Create providers
    console.log('üì¶ Creating providers...');
    let createdCount = 0;

    for (const provider of providers) {
      try {
        await prisma.provider.create({
          data: provider,
        });
        createdCount++;
        console.log(`‚úì Created: ${provider.businessName} (${provider.serviceTypes.join(', ')})`);
      } catch (error) {
        console.error(`‚úó Failed to create ${provider.businessName}:`, error);
      }
    }

    console.log(`\n‚úÖ Successfully created ${createdCount}/${providers.length} providers\n`);

    // Print summary by service type
    console.log('üìä Summary by Service Type:');
    const serviceTypeCounts: Record<string, number> = {};

    for (const provider of providers) {
      for (const serviceType of provider.serviceTypes) {
        serviceTypeCounts[serviceType] = (serviceTypeCounts[serviceType] || 0) + 1;
      }
    }

    for (const [serviceType, count] of Object.entries(serviceTypeCounts)) {
      console.log(`   ${serviceType}: ${count} providers`);
    }

    console.log('\n‚ú® Seeding complete!\n');
  } catch (error) {
    console.error('‚ùå Error seeding providers:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

// Run the seed function
seedProviders()
  .catch((error) => {
    console.error('Fatal error:', error);
    process.exit(1);
  });
